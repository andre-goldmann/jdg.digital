#!/bin/bash

# Script to process the node_modules inventory and delete marked folders

INPUT_FILE="node_modules_inventory.md"
LOG_FILE="node_modules_cleanup_$(date '+%Y%m%d_%H%M%S').log"

if [ ! -f "$INPUT_FILE" ]; then
    echo "Error: $INPUT_FILE not found!"
    echo "Please run find_node_modules.sh first."
    exit 1
fi

echo "Node Modules Cleanup Script"
echo "============================"
echo ""

# Count folders marked for deletion
delete_count=$(grep -c "| .* | \`.*\` | .* | .*DELETE" "$INPUT_FILE" 2>/dev/null || echo "0")

if [ "$delete_count" -eq 0 ]; then
    echo "No folders marked for deletion."
    echo "Please edit $INPUT_FILE and mark folders with DELETE in the Action column."
    exit 0
fi

echo "Found $delete_count folder(s) marked for deletion."
echo ""
read -p "Do you want to proceed with deletion? (yes/no): " confirm

if [ "$confirm" != "yes" ]; then
    echo "Deletion cancelled."
    exit 0
fi

echo "" | tee "$LOG_FILE"
echo "Starting deletion process..." | tee -a "$LOG_FILE"
echo "Log file: $LOG_FILE" | tee -a "$LOG_FILE"
echo "" | tee -a "$LOG_FILE"

deleted=0
failed=0

# Process each line marked for deletion
grep "| .* | \`.*\` | .* | .*DELETE" "$INPUT_FILE" | while IFS='|' read -r num path size action; do
    # Extract the path (remove backticks and whitespace)
    clean_path=$(echo "$path" | sed 's/`//g' | sed 's/^[[:space:]]*//;s/[[:space:]]*$//')

    # Convert Windows path to WSL path
    wsl_path=$(echo "$clean_path" | sed 's|\([A-Z]\):|/mnt/\L\1|')

    if [ -d "$wsl_path" ]; then
        echo "Deleting: $clean_path" | tee -a "$LOG_FILE"
        if rm -rf "$wsl_path" 2>>"$LOG_FILE"; then
            echo "  ✓ Successfully deleted" | tee -a "$LOG_FILE"
            ((deleted++))
        else
            echo "  ✗ Failed to delete" | tee -a "$LOG_FILE"
            ((failed++))
        fi
    else
        echo "Skipping (not found): $clean_path" | tee -a "$LOG_FILE"
        ((failed++))
    fi
    echo "" | tee -a "$LOG_FILE"
done

echo "===========================" | tee -a "$LOG_FILE"
echo "Cleanup Summary:" | tee -a "$LOG_FILE"
echo "  Successfully deleted: $deleted" | tee -a "$LOG_FILE"
echo "  Failed/Skipped: $failed" | tee -a "$LOG_FILE"
echo "===========================" | tee -a "$LOG_FILE"
echo ""
echo "Log saved to: $LOG_FILE"

