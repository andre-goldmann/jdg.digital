#!/bin/bash

# Script to find all node_modules folders on Windows system from WSL
# and write them to a markdown file with a table format

OUTPUT_FILE="node_modules_inventory.md"
TEMP_FILE="temp_node_modules.txt"

echo "Scanning Windows system for node_modules folders..."
echo "This may take several minutes depending on your system size."
echo ""

# Clear temp file if it exists
> "$TEMP_FILE"

# Find all node_modules directories on Windows drives
# Typically Windows drives are mounted at /mnt/c, /mnt/d, etc.
for drive in /mnt/c /mnt/d /mnt/e /mnt/f; do
    if [ -d "$drive" ]; then
        echo "Scanning drive: $drive"
        # Find node_modules folders, exclude permission denied errors
        find "$drive" -type d -name "node_modules" 2>/dev/null | while read -r folder; do
            # Convert Linux path back to Windows path format
            win_path=$(echo "$folder" | sed 's|/mnt/\([a-z]\)/|\U\1:/|')

            # Calculate folder size
            size=$(du -sh "$folder" 2>/dev/null | cut -f1)

            echo "$win_path|$size" >> "$TEMP_FILE"
            echo "Found: $win_path ($size)"
        done
    fi
done

echo ""
echo "Creating markdown file: $OUTPUT_FILE"

# Create markdown header
cat > "$OUTPUT_FILE" << 'EOF'
# Node Modules Inventory

This document lists all `node_modules` folders found on the Windows system.

**Instructions:**
- Review each entry
- Mark the **Action** column with either `DELETE` or `KEEP`
- After marking, you can use the companion cleanup script to process deletions

---

## Found Folders

| # | Path | Size | Action |
|---|------|------|--------|
EOF

# Add entries to the table
counter=1
if [ -s "$TEMP_FILE" ]; then
    while IFS='|' read -r path size; do
        echo "| $counter | \`$path\` | $size | ⬜ KEEP / DELETE |" >> "$OUTPUT_FILE"
        ((counter++))
    done < "$TEMP_FILE"
else
    echo "| - | No node_modules folders found | - | - |" >> "$OUTPUT_FILE"
fi

# Add summary section
cat >> "$OUTPUT_FILE" << EOF

---

## Summary

- **Total folders found:** $((counter - 1))
- **Scan date:** $(date '+%Y-%m-%d %H:%M:%S')
- **Scan completed from:** \`$(hostname)\`

## Next Steps

1. Review each folder path
2. Mark folders you want to delete with \`DELETE\` in the Action column
3. Mark folders you want to keep with \`KEEP\` in the Action column
4. Run the cleanup script (if available) to process deletions

## Notes

- Before deleting, ensure you have no active development in those directories
- You can always reinstall dependencies with \`npm install\` or \`yarn install\`
- Consider keeping node_modules in active projects
- Old or abandoned projects are good candidates for deletion

EOF

# Cleanup temp file
rm -f "$TEMP_FILE"

echo ""
echo "✓ Scan complete!"
echo "✓ Results written to: $OUTPUT_FILE"
echo ""
echo "Found $((counter - 1)) node_modules folder(s)"
echo ""
echo "Next steps:"
echo "1. Open $OUTPUT_FILE"
echo "2. Mark each folder as KEEP or DELETE"
echo "3. (Optional) Run the cleanup script to process deletions"

